import express from "express";
import Model from "../../models/categories/model.js";
import Brand from "../../models/categories/brandModel.js";
import Category from "../../models/categories/categoryModel.js";

const router = express.Router();

router.post("/", async (req, res) => {

    try {
        const model = await Model.create(req.body);

        res.status(201).json({
            status: "success",
            model
        });
    } catch (error) {
        res.status(500).json({
            status: "error",
            message: error.message,
        });
    }
});

router.get("/", async (req, res) => {
    try {
        const page = req.query.page || 1;
        const limit = req.query.limit || 200;
        const skip = (page - 1) * limit;

        const model = await Model.findAll({ limit, offset: skip, include: [{model: Brand}, {model: Category}]});

        res.status(200).json({
            status: "success",
            results: model.length,
            model,
        });
    } catch (error) {
        res.status(500).json({
            status: "error",
            message: error.message,
        });
    }
});

router.patch("/:id", async (req, res) => {
    try {
        const result = await Model.update(
            { ...req.body, updatedAt: Date.now() },
            {
                where: {
                    id: req.params.id,
                },
            }
        );

        if (result[0] === 0) {
            return res.status(404).json({
                status: "fail",
                message: "Server with that ID not found",
            });
        }

        const model = await Model.findByPk(req.params.id);

        res.status(200).json({
            status: "success",
            model
        });
    } catch (error) {
        res.status(500).json({
            status: "error",
            message: error.message,
        });
    }
});

router.get("/:id", async (req, res) => {
    try {
        const model = await Model.findByPk(req.params.id);

        if (!model) {
            return res.status(404).json({
                status: "fail",
                message: "Server with that ID not found",
            });
        }

        res.status(200).json({
            status: "success",
            model,
        });
    } catch (error) {
        res.status(500).json({
            status: "error",
            message: error.message,
        });
    }
});

router.delete("/:id", async (req, res) => {
    try {
        const result = await Model.destroy({
            where: { id: req.params.id },
            force: true,
        });

        if (result === 0) {
            return res.status(404).json({
                status: "fail",
                message: "Server with that ID not found",
            });
        }

        res.status(204).json();
    } catch (error) {
        res.status(500).json({
            status: "error",
            message: error.message,
        });
    }
});


export default router;